Antes de manipular cualquier dato, se necesita una base de datos. Una
base de datos es un contenedor de datos. Las bases de datos pueden
almacenar cualquier tipo de dato y objetos que permiten manipular
dichos datos como tablas, vistas, triggers y funciones.

Sintaxis:
CREATE DATABASE [IF NOT EXISTS] nombre_basedatos;

Notas:
- La claúsula IF NOT EXISTS es opcional dentro de la sentencia. L
que previene es de obtener un error cuando se crea una nueva base de
datos que ya existe en el servidor de base de datos.
